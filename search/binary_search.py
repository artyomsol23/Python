def binary_search(arr, target):
    """
    Бинарный поиск элемента target в отсортированном массиве arr.
    Возвращает индекс элемента, если он найден, или -1, если элемент отсутствует.
    
    Параметры:
        arr (list): Отсортированный массив для поиска.
        target: Искомый элемент.
        
    Возвращает:
        int: Индекс элемента или -1, если элемент не найден.
    """
    # Инициализация границ поиска
    left, right = 0, len(arr) - 1
    
    while left <= right:
        # Вычисляем середину текущего диапазона
        mid = (left + right) // 2
        
        # Если элемент найден, возвращаем его индекс
        if arr[mid] == target:
            return mid
        
        # Если средний элемент меньше искомого, ищем в правой половине
        elif arr[mid] < target:
            left = mid + 1
        
        # Если средний элемент больше искомого, ищем в левой половине
        else:
            right = mid - 1
    
    # Если элемент не найден, возвращаем -1
    return -1
