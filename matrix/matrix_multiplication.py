def matrix_multiplication(A, B):
    """
    Умножение матриц A (m × n) на B (n × k).
    Возвращает матрицу C (m × k) = A × B.
    
    Параметры:
        A (list of lists): Первая матрица размером m × n.
        B (list of lists): Вторая матрица размером n × k.
    
    Возвращает:
        list of lists: Результирующая матрица C размером m × k.
    
    Исключения:
        ValueError: Если число столбцов матрицы A не равно числу строк матрицы B.
    """
    # Проверка согласованности размеров матриц
    if len(A[0]) != len(B):
        raise ValueError("Число столбцов A должно равняться числу строк B!")
    
    # Определение размеров матриц
    m = len(A)      # Количество строк матрицы A
    n = len(A[0])    # Количество столбцов матрицы A / строк матрицы B
    k = len(B[0])    # Количество столбцов матрицы B
    
    # Создание результирующей матрицы C, заполненной нулями
    C = [[0 for _ in range(k)] for _ in range(m)]
    
    # Умножение матриц
    for i in range(m):           # Цикл по строкам матрицы A
        for j in range(k):       # Цикл по столбцам матрицы B
            for l in range(n):  # Цикл по элементам в строке/столбце
                C[i][j] += A[i][l] * B[l][j]
    
    return C
