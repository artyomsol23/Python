def matrix_scalar_multiply(A, c):
    """
    Умножает матрицу на скаляр (число).
    
    Параметры:
    - A (list of lists): Исходная матрица размером m × n, представленная как список списков.
    - c (int/float): Скалярное значение, на которое умножается каждый элемент матрицы.
    
    Возвращает:
    - list of lists: Новая матрица того же размера, где каждый элемент равен A[i][j] * c.
    
    Пример использования:
    >>> A = [[1, 2], [3, 4]]
    >>> matrix_scalar_multiply(A, 2)
    [[2, 4], [6, 8]]
    """
    # Создаем новую матрицу с помощью list comprehension для каждой строки i в матрице A и для каждого элемента j в строке i умножаем его на скаляр c
    return [[A[i][j] * c for j in range(len(A[0]))] for i in range(len(A))]
