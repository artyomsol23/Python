def is_skew_symmetric(matrix):
    """
    Проверяет, является ли матрица симметричной относительно побочной диагонали.
    
    Аргументы:
        matrix (list of lists): Входная матрица (двумерный список)
        
    Возвращает:
        bool: True если матрица симметрична относительно побочной диагонали, иначе False
    """
    n = len(matrix)
    
    # Проверка что матрица квадратная
    for row in matrix:
        if len(row) != n:
            return False  # Если строки разной длины - не квадратная
    
    # Проверка симметричности относительно побочной диагонали
    for i in range(n):  # Перебор по строкам
        for j in range(n - i):  # Перебор только элементов до побочной диагонали
            # Сравниваем симметричные элементы:
            # Элемент [i][j] должен быть равен элементу [n-1-j][n-1-i]
            if matrix[i][j] != matrix[n - 1 - j][n - 1 - i]:
                return False  # Нашли несимметричную пару
    
    # Если все проверки пройдены - матрица симметрична
    return True
