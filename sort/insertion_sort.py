def insertion_sort(arr):
    """
    Сортировка вставками (Insertion Sort).
    Проходим по массиву и на каждом шаге вставляем текущий элемент
    в правильную позицию в отсортированной части массива.
    
    Параметры:
        arr (list): Массив для сортировки.
        
    Возвращает:
        list: Отсортированный массив.
    """
    
    # Проходим по всем элементам, начиная со второго
    for i in range(1, len(arr)):
        key = arr[i]  # Текущий элемент, который нужно вставить
        j = i - 1     # Индекс предыдущего элемента
        
        # Сдвигаем элементы большие, чем key, вправо
        while j >= 0 and arr[j] > key:
            arr[j + 1] = arr[j]
            j -= 1
        
        # Вставляем key в правильную позицию
        arr[j + 1] = key
    
    return arr  # Возвращаем отсортированный массив
