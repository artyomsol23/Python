def bubble_sort(arr):
    """
    Сортировка пузырьком (пузырьковая сортировка)
    
    Параметры:
        arr (list): Список элементов для сортировки
        
    Принцип работы:
        Последовательно сравнивает соседние элементы и меняет их местами,
        если они находятся в неправильном порядке.
        Процесс повторяется, пока массив не будет отсортирован.
    """
    n = len(arr)
    
    # Проходим по всем элементам массива
    for i in range(n):
        swapped = False  # Флаг для проверки, были ли перестановки
        
        # Последние i элементов уже отсортированы и находятся на своих местах
        for j in range(0, n - i - 1):
            # Если текущий элемент больше следующего
            if arr[j] > arr[j + 1]:
                # Меняем элементы местами
                arr[j], arr[j + 1] = arr[j + 1], arr[j]
                swapped = True  # Была сделана перестановка
                
        # Если перестановок не было, массив уже отсортирован
        if not swapped:
            break  # Выход из цикла, если массив отсортирован
            
    return arr  # Возвращаем отсортированный массив
