def selection_sort(arr):
    """
    Сортировка выбором (Selection Sort)
    
    Параметры:
        arr (list): Неотсортированный список элементов
        
    Возвращает:
        list: Отсортированный по возрастанию список
    """
    
    n = len(arr)  # Получаем длину массива
    
    # Проходим по всем элементам массива
    for i in range(n):
        # Предполагаем, что первый элемент неотсортированной части минимальный
        min_idx = i
        
        # Поиск минимального элемента в оставшейся части массива
        for j in range(i + 1, n):
            if arr[j] < arr[min_idx]:
                min_idx = j  # Нашли новый минимальный элемент
        
        # Меняем местами найденный минимальный элемент 
        # с первым элементом неотсортированной части
        arr[i], arr[min_idx] = arr[min_idx], arr[i]
    
    return arr  # Возвращаем отсортированный массив
