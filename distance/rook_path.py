def rook_path(start, end):
    """
    Функция вычисляет путь ладьи из начальной позиции в конечную на шахматной доске.
    
    Параметры:
        start (tuple): Начальная позиция ладьи в формате (x1, y1).
        end (tuple): Конечная позиция ладьи в формате (x2, y2).
    
    Возвращает:
        list: Список промежуточных позиций (координат), через которые проходит ладья.
              Не включает начальную позицию, но включает конечную.
    """
    x1, y1 = start
    x2, y2 = end
    path = []

    # Движение по горизонтали (если конечная позиция не на той же вертикали)
    step_x = 1 if x2 > x1 else -1  # Определяем направление движения
    for x in range(x1, x2, step_x):
        path.append((x + step_x, y1))  # Добавляем следующий шаг по горизонтали

    # Движение по вертикали (если конечная позиция не на той же горизонтали)
    step_y = 1 if y2 > y1 else -1  # Определяем направление движения
    for y in range(y1, y2, step_y):
        path.append((x2, y + step_y))  # Добавляем следующий шаг по вертикали

    return path
