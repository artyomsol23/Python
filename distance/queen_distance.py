def queen_distance(a, b):
    """
    Вычисляет расстояние между двумя клетками на шахматной доске для ферзя.
    
    Ферзь может двигаться по горизонтали, вертикали и диагонали. 
    Расстояние определяется минимальным количеством ходов, необходимых ферзю, 
    чтобы добраться из клетки `a` в клетку `b`.
    
    Параметры:
        a (tuple): Координаты первой клетки в виде (x, y).
        b (tuple): Координаты второй клетки в виде (x, y).
    
    Возвращает:
        int: Минимальное количество ходов ферзя.
    """
    # Разница по горизонтали и вертикали
    dx = abs(a[0] - b[0])
    dy = abs(a[1] - b[1])
    
    # Если клетки на одной линии (горизонталь, вертикаль или диагональ),
    # расстояние равно максимуму из dx и dy (или их сумме, если одна из разниц равна 0)
    if dx == 0 or dy == 0 or dx == dy:
        return max(dx, dy)
    # Иначе ферзь может добраться за 2 хода (через промежуточную клетку)
    else:
        return 2
