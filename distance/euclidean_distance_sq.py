def euclidean_distance_sq(a, b):
    """
    Вычисляет квадрат евклидова расстояния между двумя точками в 2D пространстве.
    
    Параметры:
        a (tuple/list): Первая точка в формате (x1, y1)
        b (tuple/list): Вторая точка в формате (x2, y2)
    
    Возвращает:
        float: Квадрат евклидова расстояния между точками a и b
    """
    # Вычисляем разницу по координате x и возводим в квадрат
    dx_sq = (a[0] - b[0]) ** 2
    
    # Вычисляем разницу по координате y и возводим в квадрат
    dy_sq = (a[1] - b[1]) ** 2
    
    # Суммируем квадраты разниц координат
    return dx_sq + dy_sq
