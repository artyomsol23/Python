def manhattan_distance(a, b):
    """
    Вычисляет манхэттенское расстояние между двумя точками на плоскости.
    
    Параметры:
        a (tuple): Координаты первой точки в формате (x1, y1).
        b (tuple): Координаты второй точки в формате (x2, y2).
    
    Возвращает:
        int: Манхэттенское расстояние между точками a и b.
        
    Формула:
        |x1 - x2| + |y1 - y2|
    """
    # Вычисляем разницу по оси X и берем модуль
    dx = abs(a[0] - b[0])
    
    # Вычисляем разницу по оси Y и берем модуль
    dy = abs(a[1] - b[1])
    
    # Суммируем абсолютные разницы по осям
    return dx + dy
