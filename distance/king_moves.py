def king_moves(start, end):
    """
    Вычисляет минимальное количество ходов короля из начальной позиции в конечную.
    
    Параметры:
        start (tuple): Координаты начальной позиции (x1, y1).
        end (tuple): Координаты конечной позиции (x2, y2).
    
    Возвращает:
        int: Минимальное количество ходов короля.
        
    Примечание:
        Король может двигаться на одну клетку в любом направлении, включая диагональ.
        Расстояние вычисляется по метрике Чебышёва.
    """
    x1, y1 = start  # Начальная позиция
    x2, y2 = end    # Конечная позиция
    
    # Вычисляем разницу по осям X и Y
    dx = abs(x1 - x2)
    dy = abs(y1 - y2)
    
    # Расстояние Чебышёва — максимум из разниц по осям
    return max(dx, dy)
    
