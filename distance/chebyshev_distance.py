def chebyshev_distance(a, b):
    """
    Вычисляет расстояние Чебышёва между двумя точками в 2D-пространстве.

    Расстояние Чебышёва - это максимальная разность по модулю между соответствующими координатами точек.
    В шахматах соответствует минимальному числу ходов короля между двумя клетками.

    Параметры:
    a (tuple): Первая точка в виде кортежа (x1, y1)
    b (tuple): Вторая точка в виде кортежа (x2, y2)

    Возвращает:
    int или float: Расстояние Чебышёва между точками a и b

    Примеры:
    >>> chebyshev_distance((1, 2), (4, 6))
    4
    >>> chebyshev_distance((0, 0), (3, 4))
    4
    """
    # Вычисляем разности координат по каждой оси
    dx = abs(a[0] - b[0])  # Разность по оси X
    dy = abs(a[1] - b[1])  # Разность по оси Y
    
    # Возвращаем максимальную из разностей
    return max(dx, dy)
