def gcd(a, b):
    """
    Вычисляет наибольший общий делитель (НОД) двух целых чисел a и b
    с помощью алгоритма Евклида.
    
    Аргументы:
        a (int): Первое число
        b (int): Второе число
        
    Возвращает:
        int: Наибольший общий делитель чисел a и b
    
    Примеры:
        >>> gcd(54, 24)
        6
        >>> gcd(48, 18)
        6
    """
    # Пока b не равно нулю, продолжаем итерации
    while b:
        # Обновляем значения: a становится b, а b — остатком от деления a на b
        a, b = b, a % b
    
    # Когда b становится нулём, a содержит НОД
    return a
