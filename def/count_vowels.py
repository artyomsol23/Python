def count_vowels(s):
    """
    Подсчитывает количество гласных букв в переданном тексте.
    
    Учитывает как английские (a, e, i, o, u), так и русские (а, е, ё, и, о, у, ы, э, ю, я) гласные.
    Регистр символов не влияет на подсчёт.

    Параметры:
        text (str): Строка для анализа

    Возвращает:
        int: Количество гласных букв в тексте
    """
    vowels = "aeiouаеёиоуыэюя"  # Русские и английские гласные
    return sum(1 for char in s.lower() if char in vowels)
