def is_palindrome(s):
    """
    Проверяет, является ли строка палиндромом.
    
    Палиндром — это строка, которая читается одинаково 
    как слева направо, так и справа налево.
    
    Параметры:
        s (str): Строка для проверки
        
    Возвращает:
        bool: True, если строка палиндром, иначе False
    """
    # Приводим строку к нижнему регистру и удаляем пробелы
    s = s.lower().replace(" ", "")
    
    # Сравниваем строку с её обратной версией
    return s == s[::-1]
