def is_prime(n):
    """
    Проверяет, является ли число n простым.
    
    Параметры:
        n (int): Число для проверки на простоту.
        
    Возвращает:
        bool: True, если число простое, иначе False.
    """
    # Числа меньше или равные 1 не являются простыми
    if n <= 1:
        return False
    
    # Проверяем делители от 2 до квадратного корня из n
    for i in range(2, int(n ** 0.5) + 1):
        # Если n делится на i без остатка, оно не простое
        if n % i == 0:
            return False
    
    # Если делителей не найдено, число простое
    return True
