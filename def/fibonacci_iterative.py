def fibonacci_iterative(n):
    """
    Вычисляет n-ное число Фибоначчи итеративным методом.
    
    Параметры:
        n (int): Номер числа Фибоначчи (начинается с 0)
    
    Возвращает:
        int: n-ное число Фибоначчи
    """
    a, b = 0, 1  # Инициализируем первые два числа Фибоначчи
    for _ in range(n):
        a, b = b, a + b  # Обновляем значения: a становится b, b становится суммой a и b
    return a  # Возвращаем n-ное число Фибоначчи
